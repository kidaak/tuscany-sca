package org.apache.tuscany.sca.binding.ws.axis2.itests;

/**
 * Test ?wsdl works and that the returned WSDL has the correct endpoint
 *
 * @version $Rev$ $Date$
 */
public class CustomerProfileData {
    
    private String firstName;
    private String lastName;
    private String address;
    private String email;
    private String loginID;
    private String password;
    private int id;

    public CustomerProfileData(String loginID) {
        firstName = "John";
        lastName = "Doe";
        address = "345 Main Street";
        email = "john@doe.org";
        this.loginID = loginID;
        password = "hello";
        id = 1234;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String firstName) {
        this.lastName = lastName;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getLoginID() {
        return loginID;
    }

    public void setLoginID(String loginID) {
        this.loginID = loginID;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

}
